package Modelo;

public class Personal {
	
	private Integer idPersonal;
	private String nombrePer;
	private String apePatPer;
	private String apeMatPer;
	private String userName;
	private String password;
	private Integer idRol;
	private Integer numColegiado;
	private Integer idUsuario;
	
	public Personal() {
		super();
	}

	public Integer getIdPersonal() {
		return idPersonal;
	}

	public void setIdPersonal(Integer idPersonal) {
		this.idPersonal = idPersonal;
	}

	public String getNombrePer() {
		return nombrePer;
	}

	public void setNombrePer(String nombrePer) {
		this.nombrePer = nombrePer;
	}

	public String getApePatPer() {
		return apePatPer;
	}

	public void setApePatPer(String apePatPer) {
		this.apePatPer = apePatPer;
	}

	public String getApeMatPer() {
		return apeMatPer;
	}

	public void setApeMatPer(String apeMatPer) {
		this.apeMatPer = apeMatPer;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public Integer getIdRol() {
		return idRol;
	}

	public void setIdRol(Integer idRol) {
		this.idRol = idRol;
	}

	public Integer getNumColegiado() {
		return numColegiado;
	}

	public void setNumColegiado(Integer numColegiado) {
		this.numColegiado = numColegiado;
	}

	public Integer getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}
	
}
